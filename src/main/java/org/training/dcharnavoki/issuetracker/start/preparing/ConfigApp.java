package org.training.dcharnavoki.issuetracker.start.preparing;

public class ConfigApp {
	private String implStr;

	/**
	 * @param implStr
	 */
	public ConfigApp(String implStr) {
		super();
		this.implStr = implStr;
	}

	/**
	 * @return the implStr
	 */
	public String getImplStr() {
		return implStr;
	}

	/**
	 * @param implStr the implStr to set
	 */
	public void setImplStr(String implStr) {
		this.implStr = implStr;
	}

}
